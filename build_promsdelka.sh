#!/bin/bash
set -a; source /home/jarvis/kontentdesign-bot/.env; set +a
SLUG="ai-10-000-8381-fl-razrabotat-servis--sayta-dlya-ai-podbora-oborudovaniya-"
DIR="/home/jarvis/projects/demo-pages/$SLUG"
read -r -d "" PROMPT <<'EOP'
Собери ОДНОСТРАНИЧНЫЙ демо-лендинг MVP веб-сервиса Promsdelka — AI-подбор промышленного оборудования. Демо-набросок для заказчика на бирже (показать, что исполнитель прочитал ТЗ).
Структура строго по ТЗ:
1. Шапка: логотип Promsdelka, меню (Подбор, Каталог, Заявки, Поставщикам), кнопка «Оставить заявку».
2. Первый экран: заголовок про AI-подбор оборудования по запросу или ТЗ; заметный блок поиска — большое текстовое поле + кнопка загрузки файла ТЗ + кнопка «Подобрать оборудование».
3. Как работает: 4 шага (опишите запрос или загрузите ТЗ → AI извлекает параметры → предварительные варианты из каталога и интернета → оставьте заявку, менеджер пришлёт КП).
4. Блок «Актуальные заявки на оборудование»: 4 карточки-заглушки (оборудование, категория, бюджет, регион, новое/бу, срок) + кнопка «Смотреть все заявки». Без контактов покупателя.
5. Пример результата AI-подбора: 3 карточки оборудования (название, характеристики, ориентир цены, кнопка «Запросить КП»).
6. Блок для поставщиков: «Подключите каталог и получайте целевые заявки» + кнопка «Подключить каталог».
7. Футер.
Требования: ОДИН HTML-файл, всё инлайн (style в head), НИКАКИХ внешних ресурсов — только CSS-градиенты, эмодзи, SVG. Адаптив под мобилку. B2B-стиль: сдержанно, сине-стальной + один акцент, много воздуха. Тексты живые русские про промоборудование (станки, компрессоры, насосы, краны). Вверху тонкая плашка: «Демо-набросок MVP · студия Алёны · не финал».
Выведи ТОЛЬКО HTML от <!doctype html> до </html>, без markdown и пояснений.
EOP
claude -p "$PROMPT" < /dev/null > /tmp/promsdelka.raw 2>/tmp/promsdelka.err
python3 - <<PY
t=open("/tmp/promsdelka.raw",encoding="utf-8",errors="ignore").read()
i=t.lower().find("<!doctype"); j=t.lower().rfind("</html>")
if i>=0 and j>0:
    open("$DIR/index.html","w").write(t[i:j+7])
    print("OK", j+7-i)
else:
    print("FAIL", len(t))
PY
cd /home/jarvis/projects/demo-pages
export GIT_SSH_COMMAND="ssh -i /home/jarvis/.ssh/id_demo_pages -o StrictHostKeyChecking=no"
git add "$SLUG/index.html" && git commit -qm "demo Promsdelka под ТЗ заказчика" && git push -q && echo PUSHED
